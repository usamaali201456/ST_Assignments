    public String loginuser(String nm, String ps)
    {
            String  name = nm;
             String pswrd = ps;
        
        VotersList voters = VotersList.getInstance();
        
         if(name.isEmpty()||pswrd.isEmpty())
      {
          JOptionPane.showMessageDialog(this, "Fields cannot be empty... Please write your CNIC and password then try again");
          this.setVisible(true);
          return "Fields cannot be empty... Please write your CNIC and password then try again";
      
      }
        if (voters.loginVoter(name, pswrd))
        {
            loggedIn voterrr= loggedIn.getInstance();
            voterrr.setId(name);
            Voter_Home obj=new Voter_Home();
            obj.setVisible(true);
            this.setVisible(false);
            return "Successfully logged in";
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Wrong Password or Username!!!!! \n Please Try again");
            return "Wrong Password or Username!!!!! Please Try again";
        }
        
       
    }
    
